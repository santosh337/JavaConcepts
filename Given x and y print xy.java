import java.io.*;
import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        
        int n = scn.nextInt();
        
        for (int i=1;i<=n;i++) {
            int x = scn.nextInt();
            int y = scn.nextInt();
            
            System.out.println(x+""+y);
            
        }
    }
}
